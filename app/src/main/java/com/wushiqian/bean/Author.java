package com.wushiqian.bean;

public class Author {

    public Author(){
        super();
    }

    private String authorDesc;

    private String author;

    private String authorImaUrl;

    public String getAuthorDesc() {
        return authorDesc;
    }

    public void setAuthorDesc(String authorDesc) {
        this.authorDesc = authorDesc;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public String getAuthorImaUrl() {
        return authorImaUrl;
    }

    public void setAuthorImaUrl(String authorImaUrl) {
        this.authorImaUrl = authorImaUrl;
    }
}
